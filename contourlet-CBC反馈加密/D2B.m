function [a]=D2B(b)
a=zeros(1,8);
a(1)=floor(b/128);
a(2)=floor((b-a(1)*128)/64);
a(3)=floor((b-a(1)*128-a(2)*64)/32);
a(4)=floor((b-a(1)*128-a(2)*64-a(3)*32)/16);
a(5)=floor((b-a(1)*128-a(2)*64-a(3)*32-a(4)*16)/8);
a(6)=floor((b-a(1)*128-a(2)*64-a(3)*32-a(4)*16-a(5)*8)/4);
a(7)=floor((b-a(1)*128-a(2)*64-a(3)*32-a(4)*16-a(5)*8-a(6)*4)/2);
a(8)=b-a(1)*128-a(2)*64-a(3)*32-a(4)*16-a(5)*8-a(6)*4-a(7)*2;
end
